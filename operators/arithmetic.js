// Arithmetic Operators

// 1) + (Addition)

let a = 5;
let b = 5;
let res = a + b;
console.log("a + b = ",res);

// 2) - (Subtraction)

let a = 10
let b = 5
let res = a - b
console.log("a - b = ", res)

// 3) * (Multiplication)

let a = 2
let b = 2
let res = a * b
console.log("a * b = ", res)

//4) / (Division)

let a = 10
let b = 5
let res = a / b
console.log("a / b = ", res)

// 5)  % (Modulus)

let a = 5
let b = 2
res = a % b // It returns the remainder
console.log("a % b =", res) // output 1

// 6) ** (Exponentiation)

let a = 2
res = a ** 3 // 2 is multiplied by itself 3 times
console.log("2 ** 3 = ",res) // output 8

// 7) ++ (Increment)

let a = 10
res = a++ // The value of a is incremented by 1
console.log("The new value of a = ",a) // output 11

// 8) -- (Decrement)

let a = 10
res = a-- // The value of a is decremented by 1
console.log("The new value of a = ",a) // output 9 

