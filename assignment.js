// Assignment Operators

// 1) =

let a = 10 // The value 10 is assigned to the variable a (assignment left-->right)
console.log("The value of a = ",a)

// 2) +=

let a = 10
a += 2 // This statement means a = a + 2 
console.log(a) // output 12 

// 3) -=

let a = 10
a -= 2 // This statement means a = a - 2
console.log(a) // output 8 

// 4) *=

let a = 10
a *= 2 // This statement means a = a * 2
console.log(a) // output 20

// 5) %=

let a = 10
a %= 3 // This statement means a = a % 3
console.log(a) // ouput 1 

// 6) **=

let a = 2
a **= 3 // This statement means 2 * 2 * 2
console.log(a) // output 8
